     */
    public function store(Request $request)
    {
        $request->validate([
            'phone_number' => 'required|unique:customers,phone_number'
        ]);
        Customer::create($request->except('_token')+[
            'added_by' => auth()->id()
        ]);
        return back()->withSuccess('Customer Added Successfully!');
    }

